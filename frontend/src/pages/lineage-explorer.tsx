// ─── Layout helpers ──────────────────────────────────────────────────────────

/**
 * Assign each node a (depth, slot) coordinate.
 *  - root sits at depth 0
 *  - upstream ancestors get negative depth
 *  - downstream descendants get positive depth
 * Within a column, nodes from the same iteration are grouped together so
 * cross-iteration clusters read as visual blocks.
 */
function layoutByDepth<TItem extends { id: string; iterationId?: string | null; filename?: string }>(
  rootId: string,
  items: TItem[],
  edges: Array<{ source: string; target: string }>,
  opts: { colWidth: number; rowHeight: number },
) {
  const depth = new Map<string, number>()
  depth.set(rootId, 0)

  const childrenOf = new Map<string, Set<string>>()
  const parentsOf = new Map<string, Set<string>>()
  for (const e of edges) {
    if (!childrenOf.has(e.source)) childrenOf.set(e.source, new Set())
    childrenOf.get(e.source)!.add(e.target)
    if (!parentsOf.has(e.target)) parentsOf.set(e.target, new Set())
    parentsOf.get(e.target)!.add(e.source)
  }

  const dq: string[] = [rootId]
  while (dq.length) {
    const id = dq.shift()!
    const d = depth.get(id)!
    for (const c of childrenOf.get(id) ?? []) {
      const cur = depth.get(c)
      const nd = d + 1
      if (cur === undefined || nd > cur) {
        depth.set(c, nd)
        dq.push(c)
      }
    }
  }
  const uq: string[] = [rootId]
  while (uq.length) {
    const id = uq.shift()!
    const d = depth.get(id)!
    for (const p of parentsOf.get(id) ?? []) {
      const cur = depth.get(p)
      const nd = d - 1
      if (cur === undefined || nd < cur) {
        depth.set(p, nd)
        uq.push(p)
      }
    }
  }

  const byDepth = new Map<number, TItem[]>()
  for (const n of items) {
    const d = depth.get(n.id) ?? 99
    if (!byDepth.has(d)) byDepth.set(d, [])
    byDepth.get(d)!.push(n)
  }
  // Sort within each column by iterationId (so same-iteration nodes cluster
  // visually) then by filename for stability.
  for (const list of byDepth.values()) {
    list.sort((a, b) => {
      const ai = a.iterationId ?? ''
      const bi = b.iterationId ?? ''
      if (ai !== bi) return ai.localeCompare(bi)
      return (a.filename ?? '').localeCompare(b.filename ?? '')
    })
  }

  const positions = new Map<string, { x: number; y: number }>()
  for (const [d, list] of byDepth) {
    const colHeight = list.length * opts.rowHeight
    const yStart = -colHeight / 2 + opts.rowHeight / 2
    list.forEach((it, idx) => {
      positions.set(it.id, { x: d * opts.colWidth, y: yStart + idx * opts.rowHeight })
    })
  }
  return { positions, depth }
}

// ─── File-level node (default view) ──────────────────────────────────────────

interface FileNodeData extends Record<string, unknown> {
  file: LineageFile
  isRoot: boolean
  depth: number
  selected?: boolean
  onSelect: (id: string) => void
}

function FileLineageNode({ data }: { data: FileNodeData }) {
  const { file, isRoot, depth, selected, onSelect } = data
  const cls = CLASS_COLORS[file.classification ?? 'INTERNAL'] ?? CLASS_COLORS.INTERNAL
  const isUpstream = depth < 0
  const isDownstream = depth > 0
  const sizeKb = typeof file.sizeBytes === 'number' ? (file.sizeBytes / 1024).toFixed(1) : null
  // Border color follows the partner so two files from the same partner read
  // as visually grouped without needing to read labels. Falls back to a root
  // accent or neutral slate.
  const accent = file.partner?.color ?? (isRoot ? '#60a5fa' : '#475569')

  return (
    <div
      className={cn(
        'rounded-md border bg-card/95 text-foreground shadow-md w-[252px] cursor-pointer transition-all',
        isRoot && 'ring-2 ring-blue-400',
        selected && !isRoot && 'ring-2 ring-emerald-400',
        !isRoot && !selected && 'hover:border-foreground/30',
      )}
      style={{ borderColor: accent }}
      onClick={(e) => { e.stopPropagation(); onSelect(file.id) }}
      title={file.filename}
    >
      <Handle type="target" position={Position.Left} className="!w-2 !h-2 !bg-slate-500" />

      {/* Iteration ribbon - the headline piece of cross-iteration context.
          Tinted to the partner color so two files from the same iteration
          are visually obvious. A "+N" pill flags when the file is also used
          in additional iterations beyond the one that produced it. */}
      <div
        className="px-2.5 py-1 flex items-center justify-between gap-1 text-[10px] font-medium border-b"
        style={{
          background: file.partner ? `${file.partner.color}22` : 'rgba(100,116,139,0.12)',
          borderColor: file.partner ? `${file.partner.color}55` : 'rgba(100,116,139,0.3)',
          color: file.partner?.color ?? '#94a3b8',
        }}
      >
        <span className="inline-flex items-center gap-1 truncate" title={file.iterationDisplayId ?? file.iterationId}>
          <PlayCircle className="h-2.5 w-2.5 shrink-0" aria-hidden="true" />
          <span className="font-mono truncate">{file.iterationDisplayId ?? file.iterationId.slice(0, 8) + '…'}</span>
        </span>
        <span className="inline-flex items-center gap-1 shrink-0">
          {(() => {
            const otherIters = new Set(
              file.usages.map((u) => u.iterationId).filter((id) => id !== file.iterationId),
            )
            if (otherIters.size === 0) return null
            return (
              <span
                className="inline-flex items-center gap-0.5 px-1 py-0 rounded text-[8px] font-semibold border border-amber-500/50 bg-amber-500/15 text-amber-300"
                title={`Also used as input in ${otherIters.size} other iteration${otherIters.size === 1 ? '' : 's'}`}
              >
                +{otherIters.size} iter
              </span>
            )
          })()}
          {file.partner && (
            <span className="inline-flex items-center gap-0.5">
              <span className="inline-block w-1.5 h-1.5 rounded-full" style={{ background: file.partner.color }} />
              {file.partner.code}
            </span>
          )}
        </span>
      </div>

      <div className="px-2.5 py-2 space-y-1.5">
        <div className="flex items-start gap-1.5">
          <FileText className={cn('h-3.5 w-3.5 mt-0.5 shrink-0', isRoot ? 'text-blue-400' : 'text-muted-foreground')} aria-hidden="true" />
          <div className="min-w-0 flex-1">
            <p className="text-[11px] font-semibold truncate" title={file.filename}>{file.filename}</p>
            <p className="text-[9px] text-muted-foreground font-mono truncate" title={file.id}>
              {file.id.slice(0, 8)}…
            </p>
          </div>
        </div>
        <div className="flex items-center gap-1 flex-wrap">
          <span
            className={cn(
              'text-[8px] font-semibold uppercase tracking-wide rounded px-1.5 py-0.5 border',
              cls.bg, cls.text, cls.border,
            )}
          >
            <Lock className="h-2 w-2 inline mr-0.5" aria-hidden="true" />
            {file.classification ?? 'INTERNAL'}
          </span>
          {isRoot && <Badge variant="outline" className="text-[8px] h-3.5 px-1 leading-none border-blue-500/50 text-blue-300">root</Badge>}
          {isUpstream && <Badge variant="outline" className="text-[8px] h-3.5 px-1 leading-none">upstream · d{Math.abs(depth)}</Badge>}
          {isDownstream && <Badge variant="outline" className="text-[8px] h-3.5 px-1 leading-none">downstream · d{depth}</Badge>}
        </div>
        {file.nodeSourceLabel && (
          <p className="text-[9px] text-muted-foreground truncate">
            from <span className="font-mono">{file.nodeSourceLabel}</span>
          </p>
        )}
        {sizeKb && (
          <p className="text-[9px] text-muted-foreground/80 tabular-nums">{sizeKb} KB</p>
        )}
      </div>
      <Handle type="source" position={Position.Right} className="!w-2 !h-2 !bg-slate-500" />
    </div>
  )
}

