/**
 * Cross-iteration usages list. Groups OUTPUT + INPUT refs by iteration so the
 * reader sees one row per (iteration, role) pair - answers "this file is
 * produced in iteration A and used as input in iterations B and C".
 */
function UsagesList({
  file,
  navigate,
}: {
  file: LineageFile
  navigate: ReturnType<typeof useNavigate>
}) {
  const grouped = useMemo(() => {
    type Row = {
      iterationId: string
      iterationDisplayId: string | null
      iterationStatus: string | null
      role: LineageUsage['role']
      nodes: Array<{ nodeId: string; nodeLabel: string | null; inputId: string | null; outputId: string | null }>
    }
    const byKey = new Map<string, Row>()
    for (const u of file.usages) {
      const key = `${u.iterationId}|${u.role}`
      const row =
        byKey.get(key) ??
        ({
          iterationId: u.iterationId,
          iterationDisplayId: u.iterationDisplayId,
          iterationStatus: u.iterationStatus,
          role: u.role,
          nodes: [],
        } satisfies Row)
      row.nodes.push({
        nodeId: u.nodeId,
        nodeLabel: u.nodeLabel,
        inputId: u.inputId,
        outputId: u.outputId,
      })
      byKey.set(key, row)
    }
    // OUTPUT rows first, then INPUT rows; producing iteration first within each role.
    return Array.from(byKey.values()).sort((a, b) => {
      if (a.role !== b.role) return a.role === 'OUTPUT' ? -1 : 1
      if (a.iterationId === file.iterationId) return -1
      if (b.iterationId === file.iterationId) return 1
      return (a.iterationDisplayId ?? a.iterationId).localeCompare(b.iterationDisplayId ?? b.iterationId)
    })
  }, [file])

  if (grouped.length === 0) return null

  const otherIterationCount = new Set(
    file.usages.map((u) => u.iterationId).filter((id) => id !== file.iterationId),
  ).size

  return (
    <div>
      <p className="text-[10px] uppercase tracking-wider text-muted-foreground mb-1 inline-flex items-center gap-1">
        <PlayCircle className="h-2.5 w-2.5" />
        Used in iterations ({new Set(file.usages.map((u) => u.iterationId)).size})
        {otherIterationCount > 0 && (
          <span className="text-amber-300 normal-case font-normal">
            · {otherIterationCount} beyond the one that produced it
          </span>
        )}
      </p>
      <ul className="space-y-1">
        {grouped.map((row) => {
          const isOwn = row.iterationId === file.iterationId
          return (
            <li
              key={`${row.iterationId}-${row.role}`}
              className="rounded border border-border/60 bg-muted/20 px-2 py-1.5 text-[10px] space-y-0.5"
            >
              <div className="flex items-center justify-between gap-2">
                <button
                  type="button"
                  onClick={() => navigate(`/iteration/${row.iterationId}`)}
                  className="font-mono font-medium hover:text-foreground truncate text-left"
                  title={row.iterationId}
                >
                  {row.iterationDisplayId ?? row.iterationId.slice(0, 8) + '…'}
                </button>
                <span
                  className={cn(
                    'inline-flex items-center gap-0.5 px-1.5 py-0 rounded text-[9px] font-semibold uppercase tracking-wide border shrink-0',
                    row.role === 'OUTPUT'
                      ? 'bg-emerald-500/15 text-emerald-300 border-emerald-500/40'
                      : 'bg-blue-500/15 text-blue-300 border-blue-500/40',
                  )}
                >
                  {row.role === 'OUTPUT' ? (isOwn ? 'produced here' : 'produced') : 'used as input'}
                </span>
              </div>
              <div className="text-muted-foreground flex flex-wrap gap-x-2 gap-y-0.5">
                {row.nodes.map((n, i) => (
                  <span key={i} className="inline-flex items-center gap-0.5">
                    <span className="opacity-60">on</span>
                    <span className="font-mono">{n.nodeLabel ?? n.nodeId}</span>
                    {row.role === 'INPUT' && n.inputId && (
                      <span className="opacity-60">(slot {n.inputId})</span>
                    )}
                    {row.role === 'OUTPUT' && n.outputId && (
                      <span className="opacity-60">(slot {n.outputId})</span>
                    )}
                  </span>
                ))}
              </div>
              {row.iterationStatus && (
                <span className="inline-flex items-center text-[9px] text-muted-foreground/80">
                  {row.iterationStatus}
                </span>
              )}
            </li>
          )
        })}
      </ul>
    </div>
  )
}

